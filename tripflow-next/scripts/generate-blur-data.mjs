import sharp from 'sharp';
import { readdir, writeFile } from 'fs/promises';
import { join } from 'path';

const IMG_DIR = join(import.meta.dirname, '../public/images/itinerary');
const OUTPUT = join(import.meta.dirname, '../src/lib/blur-data.ts');

async function main() {
  const files = await readdir(IMG_DIR);
  // Use -400 size as source (smallest available)
  const sources = files.filter(f => f.endsWith('-400.webp'));

  const entries = [];
  for (const file of sources) {
    const name = file.replace('-400.webp', '');
    const buf = await sharp(join(IMG_DIR, file))
      .resize(10, 10, { fit: 'cover' })
      .webp({ quality: 20 })
      .toBuffer();
    const b64 = `data:image/webp;base64,${buf.toString('base64')}`;
    entries.push(`  '${name}': '${b64}'`);
  }

  const code = `// Auto-generated by scripts/generate-blur-data.mjs — do not edit manually
const BLUR_DATA: Record<string, string> = {
${entries.join(',\n')},
};

/**
 * Returns a tiny base64 blur data URL for an itinerary image path.
 * Works with paths like "/images/itinerary/bund-800.webp".
 */
export function getBlurDataURL(imageUrl: string): string | undefined {
  const match = imageUrl.match(/^\\/images\\/itinerary\\/(.+)-\\d+\\.webp$/);
  return match ? BLUR_DATA[match[1]] : undefined;
}
`;

  await writeFile(OUTPUT, code, 'utf-8');
  console.log(`Generated blur data for ${entries.length} images -> ${OUTPUT}`);
}

main();
